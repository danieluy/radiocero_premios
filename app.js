"use strict"
////////////////////////////////////////////////////////////////////////////////
//  Dependencies  //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
const express = require('express');
const app = express();
const server = require('http').createServer(app);
const router = require('./routers/router.js');
const api_router = require('./routers/api-router.js');
const fs = require('fs');
const config = require('./config.json');
const sessions = require('client-sessions');
const boot = require('./my_modules/boot.js');
const ip = require('ip');
////////////////////////////////////////////////////////////////////////////////
//  Configuration  /////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
// Server parameters
const srv_params = {
	ip: config.network.port || ip.address(),
	port: config.network.port || '1043',
	url: function() { return 'http://' + this.ip + ':' + this.port }
}
// Session parameters
const session = {
	secret: config.session.secret,
	duration: config.session.durationHours,
	active_duration: config.session.activeDurationHours
}
// View engine
app.set('view engine', 'ejs');
app.set('views', __dirname + '/views');
////////////////////////////////////////////////////////////////////////////////
//  Middleware  ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//  Public folder
app.use(express.static(__dirname + '/public'));
// Session
app.use(sessions({
	cookieName: 'session',
	secret: session.secret,
	duration: session.duration * 60 * 60 * 1000, // hours
	activeDuration: session.active_duration * 60 * 60 * 1000, // hours
	cookie: {
		ephemeral: false,
		httpOnly: false,
		secure: false
	}
}));
// Routing
app.use('/', router);
app.use('/api', api_router);
////////////////////////////////////////////////////////////////////////////////
//  Server  ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
const start_server = () => {
	server.listen(srv_params.port, () => {
		console.log('Listening on: ' + srv_params.url());
		console.log('Press Ctrl-C to terminate');
	});
}
////////////////////////////////////////////////////////////////////////////////
//  Boot  //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
boot.checkAdmin(start_server);